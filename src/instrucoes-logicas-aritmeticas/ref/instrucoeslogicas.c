int main(){
	int x = 2;
	int y = 1;

	// Operações lógicas
	int maior = (x > y);
	int menor = (x < y);
	int maiorIgual = (x >= y);
	int menorIgual = (x <= y);
	int igual = (x == y);
	int diferente = (x != y);
	int and = (x & y);
	int or = (x | y);
	int not = !x;
	int xor = (x ^ y);

	// Operações aritméticas
	int soma = (x + y);
	int subtracao = (x - y);
	int multiplicacao = (x * y);
	int divisao = (x / y);
	int modulo = (x % y);
	int shiftDireita = (x >> 1);
	int shiftEsquerda = (x << 1);

	return 0;
}
